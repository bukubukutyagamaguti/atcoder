import math

"""
平方根を返す
"""
num = math.sqrt(5)
# print(num) 2.23606797749979

"""
x以下の最大の整数を返す
切り捨て
"""
num = math.floor(3.3)
# print(num) 3


"""
x以上の最小の整数を返す
切り上げ
"""
num = math.ceil(3.3)
# print(num) 4

"""
小数点を切り捨て
"""
num = math.trunc(3.3)
# print(num) 3

"""
x乗を返す
"""
num = math.exp(3)
# print(num) 20.085536923187668

"""
xのy乗を返す
"""
num = math.pow(3, 2)
# print(num) 9.0
